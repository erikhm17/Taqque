    private void btnGuardarTablaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarTablaActionPerformed
        // TODO add your handling code here:
        List<Atributo> lista = modeloTabla.getListaAtributos();
        Tabla tabla = new Tabla(this.txtNombreTabla.getText(),
                lista);
        EntradaCatalogo entrada =
                new EntradaCatalogo(tabla, catalogo.getDirectorio());
        catalogo.crearNuevaEntrada(entrada);
        actualizarCatalogo();

    }//GEN-LAST:event_btnGuardarTablaActionPerformed

    private void btnEliminarTablaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarTablaActionPerformed
        // TODO add your handling code here:
        int idx = this.lstListaTablas.getSelectedIndex();
        EntradaCatalogo entrada = (EntradaCatalogo) modeloLista.get(idx);
        catalogo.eliminarTabla(entrada.getNombreTabla());
        actualizarCatalogo();
    }//GEN-LAST:event_btnEliminarTablaActionPerformed

    private void btnAbrirTablaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbrirTablaActionPerformed
        // TODO add your handling code here:
        int idx = this.lstListaTablas.getSelectedIndex();
        EntradaCatalogo entrada = (EntradaCatalogo) modeloLista.get(idx);
        
        AbrirTablaIU abrirTablaIU = new AbrirTablaIU(catalogo,entrada);
        abrirTablaIU.setVisible(true);
    }//GEN-LAST:event_btnAbrirTablaActionPerformed

    private void actualizarCatalogo() {
        try {
            catalogo.escribirCatalogo();
            catalogo.leerCatalogo();
            modeloTabla.limpiarModeloTabla();
            this.mostrarInformacionCatalogo();
            this.txtNombreTabla.setText("");
        } catch (Exception ex) {
            Logger.getLogger(BaseDatosIU.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

