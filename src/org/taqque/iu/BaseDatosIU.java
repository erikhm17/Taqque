    private void llenarTipos()
    {
        cboTipo.addItem(String.class);
        cboTipo.addItem(Integer.class);
        cboTipo.addItem(Float.class);
        cboTipo.addItem(Double.class);
        cboTipo.addItem(Character.class);
        cboTipo.addItem(Boolean.class);
        cboTipo.addItem(Long.class);
    }

    private void mostrarInformacionCatalogo() {
        this.txtNombreBD.setText(catalogo.getPath());
        modeloLista.clear();
        Collection<EntradaCatalogo> lista = catalogo.getEntradas().values();
        for (EntradaCatalogo entrada : lista) {
            modeloLista.addElement(entrada);
        }
        lstListaTablas.setModel(modeloLista);
    }

    private void informacionTabla(EntradaCatalogo entrada) {
        modeloTabla.limpiarModeloTabla();
        Iterator<Atributo> iterator = entrada.getTabla().iterator();
        while (iterator.hasNext()) {
            Atributo atributo = iterator.next();
            modeloTabla.addRow(atributo);
        }
        tablaInfoTabla.setModel(modeloTabla);
    }

